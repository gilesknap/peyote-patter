def rotate_90cw(glyph: list[list[int]]) -> list[list[int]]:
    """Rotate a glyph 90 degrees clockwise.

    Input:  H rows x W cols (reading orientation)
    Output: W rows x H cols (fabric orientation)
    """
    h = len(glyph)
    w = len(glyph[0])
    rotated = []
    for c in range(w):
        row = []
        for r in range(h - 1, -1, -1):
            row.append(glyph[r][c])
        rotated.append(row)
    return rotated


def render_text_rows(text: str) -> list[list[int]]:
    """Render text to rotated 10-wide pixel rows using bitmap glyphs."""
    text = text.upper()
    rows: list[list[int]] = []
    for i, ch in enumerate(text):
        if i > 0:
            for _ in range(CHAR_SPACING):
                rows.append([0] * GLYPH_H)
        glyph = GLYPHS.get(ch, GLYPHS[' '])
        rotated = rotate_90cw(glyph)
        rows.extend(rotated)
    return rows
